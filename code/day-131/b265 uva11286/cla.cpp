#include <cstdio>
#include <unordered_map>
#include <algorithm>
using namespace std;

inline int read(){
    int x=0;
    char c=0;
    while(c<'0'||c>'9'){
        c=getchar();
        if(c==-1){return 0;}
    }
    while(c>='0'&&c<='9'){
        x=(x<<3)+(x<<1)+(c-'0');
        c=getchar();
    }
    return x;
}
inline void write(int x){
    if(x>=10){write(x/10);}
    putchar(x%10+'0');
}
//-block
signed main(){
    int N;
    while(N=read()){
        unordered_map<int,int>mp;
        while(N--){
            int dat[5];
            for(int i=0;i<5;++i){
                dat[i]=read();
            }
            sort(dat,dat+5);

            for(int i=1;i<5;++i){
                dat[i]+=dat[i-1]*1000;
            }
            ++mp[dat[4]];
        }
        int maxx=0,max_cnt=1;
        for(auto it=mp.cbegin();it!=mp.cend();++it){
            if(it->second > maxx){
                maxx=it->second;
                max_cnt=1;
            }else if(it->second==maxx){
                ++max_cnt;
            }
        }
        write(maxx*max_cnt);
        putchar('\n');
    }
    return 0;
}
